    - **비트코인:** 사토시 나카모토는 사라졌습니다. 이것이 비트코인의 가장 강력한 무기입니다. 리더가 없으므로 누구도 비트코인의 규칙(2,100만 개 발행 등)을 마음대로 바꿀 수 없습니다. 이는 비트코인을 인위적인 ''발명품''이라기보다 자연 상태의 금처럼 **''발견된 자원''**에 가깝게 만듭니다.
    
    ### 2. 열역학적 보안 장벽 (Hash Rate)
    
    비트코인을 ''물리학적 화폐''라고 부르는 이유는, 비트코인 네트워크를 지키기 위해 전 세계적으로 엄청난 양의 전기와 연산 장비가 투입되고 있기 때문입니다.
    
    - **복제의 한계:** 누군가 비트코인과 똑같은 코드를 복사해서 ''뉴(New) 비트코인''을 만들었다고 가정해 봅시다. 코드는 똑같을지 몰라도, 그 네트워크를 지키는 **채굴기(에너지)**는 0에서 시작해야 합니다.
    - **보안 격차:** 현재 비트코인 네트워크를 해킹하려면 국가급의 전력이 필요합니다. 하지만 새로 만든 코인은 적은 에너지로도 쉽게 공격당하고 조작될 수 있습니다. 이미 압도적인 에너지 장벽(해시레이트)을 쌓아 올린 비트코인을, 후발 주자가 물리적으로 따라잡는 것은 거의 불가능에 가깝습니다.
    
    ### 3. 린디 효과 (The Lindy Effect)와 경로 의존성
    
